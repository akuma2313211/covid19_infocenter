                                                <h3 class="card-title h3 mb-3 text-left">A Closer Look at How COVID-19 mRNA Vaccines Work</h3>
                                                <p>COVID-19 mRNA vaccines give instructions for our cells to make a harmless piece of what is called the “spike protein.” 
												The spike protein is found on the surface of the virus that causes COVID-19.</p>
                                                
                                               <p>COVID-19 mRNA vaccines are given in the upper arm muscle. 
											   Once the instructions (mRNA) are inside the immune cells,
											   the cells use them to make the protein piece. After the protein piece is made,
											   the cell breaks down the instructions and gets rid of them.</p>
											   <br>
											   <p>Next, the cell displays the protein piece on its surface.
											   Our immune systems recognize that the protein doesn’t belong there and begin building an immune response and making antibodies,
											   like what happens in natural infection against COVID-19.</p>
											   <br>
											   <p>At the end of the process, our bodies have learned how to protect against future infection. 
											   The benefit of mRNA vaccines, like all vaccines,
											   is those vaccinated gain this protection without ever having to risk the serious consequences of getting sick with COVID-19.
											   </p>
