                                        <h2 class="card-title h3 mb-3 text-left">What is different about mRNA vaccines?</h2>
                                        <p>Some vaccines use a whole virus or bacterium to teach our bodies how to build up immunity to the pathogen.
										These pathogens are inactivated or attenuated, which means weakened. Other vaccines use parts of viruses or bacteria.</p>
                                        <br>
										<p>Recombinant vaccine technology employs yeast or bacterial cells to made many copies of a particular viral
										or bacterial protein or sometimes a small part of the protein.</p>
										<br>
                                        <p>mRNA vaccines bypass this step. They are chemically synthesized without the need for cells or pathogens,
										making the production process simpler. mRNA vaccines carry the information that allows our own cells to make the pathogen’s proteins or protein fragments themselves.</p>
                                        <br>
										<p>
										Importantly, mRNA vaccines only carry the information to make a small part of a pathogen. From this information, it is not possible for our cells to make the whole pathogen.
										</p>
										<br>
										<p><b>
										Both mRNA COVID-19 vaccines that Pfizer/BioNTech and Moderna have developed cannot cause COVID-19.
										They do not carry the full information for our cells to make the SARS-CoV-2 virus, and therefore, cannot cause an infection.<b>
