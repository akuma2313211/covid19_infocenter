                                        <h2 class="card-title h3 mb-3 text-left">Types of Vaccines</h2>
										<p>Currently, there are three main types of COVID-19 vaccines that are authorized and recommended, or undergoing large-scale (Phase 3) clinical trials in the United States.
										Below is a description of how each type of vaccine prompts our bodies to recognize and protect us from the virus that causes COVID-19.
										None of these vaccines can give you COVID-19.</p>
										<br>
                                        <ul>
                                            <li><b>mRNA</b> vaccines contain material from the virus that causes COVID-19 that gives
											our cells instructions for how to make a harmless protein that is unique to the virus.
											After our cells make copies of the protein, they destroy the genetic material from the vaccine.
											Our bodies recognize that the protein should not be there and build T-lymphocytes and B-lymphocytes
											that will remember how to fight the virus that causes COVID-19 if we are infected in the future.</li>
                                            <li><b>Protein subunit vaccines</b> include harmless pieces (proteins) of the virus that causes COVID-19 instead
											of the entire germ. Once vaccinated, our bodies recognize that the protein should not be there and build T-lymphocytes
											and antibodies that will remember how to fight the virus that causes COVID-19 if we are infected in the future.</li>
											<li><b>Vector vaccines</b> contain a modified version of a different virus than the one that causes COVID-19. 
											Inside the shell of the modified virus, there is material from the virus that causes COVID-19. 
											This is called a “viral vector.” Once the viral vector is inside our cells, 
											the genetic material gives cells instructions to make a protein that is unique to the virus that causes COVID-19.
											Using these instructions, our cells make copies of the protein. This prompts our bodies to build T-lymphocytes and
											B-lymphocytes that will remember how to fight that virus if we are infected in the future.</li>
                                        </ul>

