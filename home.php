                                        <h2 class="card-title h3 mb-3 text-left">How COVID-19 Vaccines Work</h2>
                                        <p>COVID-19 vaccines help our bodies develop immunity to the virus that causes COVID-19 without us having to get the illness. 
										Different types of vaccines work in different ways to offer protection.
										But with all types of vaccines, the body is left with a supply of “memory” T-lymphocytes as well as B-lymphocytes that will remember how to fight that virus in the future.</p>
                                        <br>
										<p>It typically takes a few weeks after vaccination for the body to produce T-lymphocytes and B-lymphocytes. Therefore, 
										it is possible that a person could be infected with the virus that causes COVID-19 just before or just after vaccination
										and then get sick because the vaccine did not have enough time to provide protection.</p>
										<br>
                                        <p>Sometimes after vaccination, the process of building immunity can cause symptoms, such as fever. 
										These symptoms are normal and are signs that the body is building immunity.</p>
                                    
