                                                <h3 class="card-title h3 mb-3 text-left">The Immune System—the Body’s Defense Against Infection</h3>
                                                <p>To understand how COVID-19 vaccines work, it helps to first look at how our bodies fight illness.
												When germs, such as the virus that causes COVID-19, invade our bodies, they attack and multiply.
												This invasion, called an infection, is what causes illness. Our immune system uses several tools to fight infection.
												Blood contains red cells, which carry oxygen to tissues and organs, and white or immune cells, which fight infection.
												Different types of white blood cells fight infection in different ways:</p>
                                                <ul>
													<li><b>Macrophages</b> are white blood cells that swallow up and digest germs and dead or dying cells.
													The macrophages leave behind parts of the invading germs, called “antigens”.
													The body identifies antigens as dangerous and stimulates antibodies to attack them.</li>
													<li><b>B-lymphocytes</b> are defensive white blood cells. They produce antibodies that attack the pieces of the virus left behind by the macrophages.</li>
													<li><b>T-lymphocytes</b> are another type of defensive white blood cell. They attack cells in the body that have already been infected.</li>
												</ul>
                                               <p>The first time a person is infected with the virus that causes COVID-19, it can take several
											   days or weeks for their body to make and use all the germ-fighting tools needed to get over the infection.
											   After the infection, the person’s immune system remembers what it learned about how to protect the body against that disease.</p>
											   <br>
											   <p>The body keeps a few T-lymphocytes, called “memory cells”, that go into action quickly if the body encounters the same virus again.
											   When the familiar antigens are detected, B-lymphocytes produce antibodies to attack them.
											   Experts are still learning how long these memory cells protect a person against the virus that causes COVID-19.</p>
